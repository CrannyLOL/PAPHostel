// Obter idioma atual do localStorage
export function getCurrentLanguage() {
  return localStorage.getItem("language") || "pt";
}

// Definir novo idioma
export function setLanguage(lang) {
  localStorage.setItem("language", lang);
}

// Obter texto traduzido
export function t(key) {
  const lang = getCurrentLanguage();
  return translations[lang][key] || translations["pt"][key] || key;
}

// Traduzir página inteira
export function translatePage() {
  // Traduzir elementos pela classe
  document.querySelectorAll("[data-i18n]").forEach(element => {
    const key = element.getAttribute("data-i18n");
    element.textContent = t(key);
  });

  // Traduzir atributos
  document.querySelectorAll("[data-i18n-placeholder]").forEach(element => {
    const key = element.getAttribute("data-i18n-placeholder");
    element.placeholder = t(key);
  });

  document.querySelectorAll("[data-i18n-title]").forEach(element => {
    const key = element.getAttribute("data-i18n-title");
    element.title = t(key);
  });

  // Atualizar lang attribute da página
  document.documentElement.lang = getCurrentLanguage() === "pt" ? "pt-PT" : "en-GB";
}

// Setup do toggle de idioma
export function setupLanguageToggle() {
  // Botões PT e EN
  const btnPt = document.getElementById("langBtnPt");
  const btnEn = document.getElementById("langBtnEn");
  
  if (btnPt) {
    btnPt.addEventListener("click", () => {
      setLanguage("pt");
      updateLanguageButtons();
      translatePage();
    });
  }
  
  if (btnEn) {
    btnEn.addEventListener("click", () => {
      setLanguage("en");
      updateLanguageButtons();
      translatePage();
    });
  }
  
  updateLanguageButtons();
}

// Atualizar aparência dos botões de idioma
function updateLanguageButtons() {
  const btnPt = document.getElementById("langBtnPt");
  const btnEn = document.getElementById("langBtnEn");
  const currentLang = getCurrentLanguage();
  
  if (btnPt) {
    if (currentLang === "pt") {
      btnPt.style.opacity = "1";
      btnPt.style.boxShadow = "0 0 12px rgba(0, 198, 255, 0.8)";
    } else {
      btnPt.style.opacity = "0.6";
      btnPt.style.boxShadow = "0 2px 8px rgba(0,0,0,0.3)";
    }
  }
  
  if (btnEn) {
    if (currentLang === "en") {
      btnEn.style.opacity = "1";
      btnEn.style.boxShadow = "0 0 12px rgba(0, 198, 255, 0.8)";
    } else {
      btnEn.style.opacity = "0.6";
      btnEn.style.boxShadow = "0 2px 8px rgba(0,0,0,0.3)";
    }
  }
}
